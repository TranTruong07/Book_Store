package models;

/**
 *
 * @author Admin
 */
public class Category {
    private int id;
    private String name;
    private int statusId;
    private Status status;

    public Category(int id, String name, int statusId) {
        this.id = id;
        this.name = name;
        this.statusId = statusId;
    }

    public Category(int id, String name, int statusId, Status status) {
        this.id = id;
        this.name = name;
        this.statusId = statusId;
        this.status = status;
    }

    public int getStatusId() {
        return statusId;
    }

    public void setStatusId(int statusId) {
        this.statusId = statusId;
    }

    public Status getStatus() {
        return status;
    }

    public void setStatus(Status status) {
        this.status = status;
    }

    

    public Category() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
